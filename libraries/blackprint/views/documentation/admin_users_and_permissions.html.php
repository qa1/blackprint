		<h3>Users & Permissions</h3>
		<p>
			By default Blackprint uses a basic set of user roles and permissions which are coded into the CMS and leave very little up to the database. 
			Using this strategy, the CMS is far more secure than if it were to use a traditional RBAC method which keeps rules in a database. The number of ways 
			an administrative user can be created are limited and therefore even if part of the system was to be compromised, only a minimal amount of damage 
			could be done.
		</p>
		<p>
			Of course another major goal is to never limit the system or prevent it from meeting the needs its users. So additional access control systems can 
			be put into place as well. 
